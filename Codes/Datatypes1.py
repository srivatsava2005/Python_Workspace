# Python Program to Demonstrate Data Types

# Integer
a = 10
print("Value:", a, "| Type:", type(a))

# Float
b = 10.5
print("Value:", b, "| Type:", type(b))

# String
c = "Hello Python"
print("Value:", c, "| Type:", type(c))

# Boolean
d = True
print("Value:", d, "| Type:", type(d))

# List
e = [1, 2, 3, 4]
print("Value:", e, "| Type:", type(e))

# Tuple
f = (10, 20, 30)
print("Value:", f, "| Type:", type(f))

# Set
g = {10, 20, 30}
print("Value:", g, "| Type:", type(g))

# Dictionary
h = {"name": "Alice", "age": 25}
print("Value:", h, "| Type:", type(h))

# NoneType
i = None
print("Value:", i, "| Type:", type(i))
