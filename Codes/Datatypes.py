# Numbers
a = 10        # int
b = 3.5       # float

# String
s = "Hello"

# Boolean
flag = True

# List
lst = [1, 2, 3]

# Tuple
tup = (4, 5, 6)

# Dictionary
d = {"name": "Alex", "age": 21}

# Set
st = {1, 2, 3}

# None
n = None

# Print all with types
print(type(a), a)
print(type(b), b)
print(type(s), s)
print(type(flag), flag)
print(type(lst), lst)
print(type(tup), tup)
print(type(d), d)
print(type(st), st)
print(type(n), n)
