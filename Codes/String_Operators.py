# String Operators in Python

# 1. Concatenation (+)
str1 = "Hello"
str2 = "World"
result1 = str1 + " " + str2
print("Concatenation:", result1)

# 2. Repetition (*)
result2 = str1 * 3
print("Repetition:", result2)

# 3. Membership (in / not in)
print("'H' in str1:", 'H' in str1)
print("'z' not in str2:", 'z' not in str2)

# 4. Indexing
print("First character of str1:", str1[0])
print("Last character of str2:", str2[-1])

# 5. Slicing
print("First 3 chars of str1:", str1[0:3])
print("From index 1 to end in str2:", str2[1:])
